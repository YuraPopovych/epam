Змінна $min має число від 0 до 59. Визначити в яку чверть години потрапляє число, відповідно вивести на екран одне з значень: «перша», «друга», «третя» або «четверта».
*/

for ($i = 0;  $i < 60; $i++ ){
    $min[] = $i;
}

foreach ($min as $i){
    if($i<15){
        echo "перша: $i". "<br>";
    }
    elseif ($i < 30){
        echo "друга: $i". "<br>";
    }
    elseif ($i < 45){
        echo "третя: $i". "<br>";
    }
    else{
        echo "четверта: $i". "<br>";
    }
}


