Є рядок з 6-ти чисел (наприклад: 385934).
Перевірте, чи сума перших трьох чисел дорівнює сумі других трьох чисел. Якщо це так – виведіть 'так' інакше – 'ні'
*/

$first = 0;
$second = 0;
$a = '385934';

for($i = 0; $i < 3; $i++){
    $first += $a[$i];
}

for($i = 3; $i < strlen($a); $i++){
    $second += $a[$i];
}
if ($first === $second){
    echo 'так';
}
else{
    echo 'ні';
}

